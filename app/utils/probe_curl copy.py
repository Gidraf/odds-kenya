# ─── Public API ──────────────────────────────────────────────────────────────
# ═══════════════════════════════════════════════════════════════════════════════

def _probe(
    url:     str,
    method:  str         = "GET",
    headers: dict | None = None,
    params:  dict | None = None,
    body:    Any         = None,
    timeout: int         = 30,
) -> dict:
    """
    Fire an HTTP request. Returns a structured result dict.

    Uses Playwright (headless Chromium) when available so that
    per-request HMAC tokens (e.g. 1xbet x-hd) are generated by the
    site's own JS. Falls back to curl_cffi if Playwright is not installed,
    but note that curl_cffi CANNOT handle sites requiring x-hd — it will
    always return 406 for those endpoints.

    KEY BEHAVIOUR: Any header in _SIGNATURE_HEADERS (x-hd, x-signature etc.)
    is ALWAYS stripped before the request is fired, even if it was present in
    the saved session config. This ensures cached curl configs never replay a
    stale token — the browser regenerates a fresh one every time.

    Same signature and response shape regardless of backend — safe to
    call from Flask views, Celery tasks, or CLI scripts.
    """
    
    # 🚨 ANTI-BOT FIX: Preserve query parameter order
    # When loading params from a database (like JSONB), or passing them into 
    # underlying libraries (like curl_cffi), the dictionary keys often get 
    # sorted alphabetically. We build the query string manually right here 
    # to lock in the exact insertion order so the WAF receives the 
    # specific URL fingerprint it expects.
    if params:
        clean_params = {str(k): str(v) for k, v in params.items() if str(k).strip()}
        if clean_params:
            qs = urllib.parse.urlencode(clean_params, quote_via=urllib.parse.quote)
            sep = "&" if "?" in url else "?"
            url = f"{url}{sep}{qs}"
        
        # Setting params to None forces the backends to use the raw pre-assembled URL 
        # instead of attempting to rebuild (and incorrectly sort) them.
        params = None

    if _PLAYWRIGHT_OK:
        return _probe_playwright(url, method, headers, params, body, timeout)
    if _CURL_OK:
        return _probe_curl(url, method, headers, params, body, min(timeout, 15))
    
    return _error_result(url, "Neither playwright nor curl_cffi is installed. "
                         "Run: pip install playwright && playwright install chromium", 0)


