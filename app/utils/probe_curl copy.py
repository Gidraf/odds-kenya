"""
probe_utils.py  (Playwright edition)
=====================================
Drop-in replacement — same _probe() signature, same response dict shape.

Why Playwright:
  Sites like 1xbet use per-request HMAC signatures (x-hd) generated by
  their own JavaScript at call time. The token expires in ~1 second so it
  can never be replayed from saved headers. Playwright runs real Chromium
  so the JS runs natively and signs every request correctly.

Backend selection (automatic):
  1. Playwright   — if installed  (pip install playwright && playwright install chromium)
  2. curl_cffi    — fallback if Playwright not installed

Both backends return the same dict so nothing else in the codebase changes.
Celery tasks call _probe() directly — same function, just runs headless.

Install:
    pip install playwright --break-system-packages
    playwright install chromium

KEY FIX (v2):
  - x-hd and any other HMAC/signature headers are now ALWAYS stripped
    server-side before the request is fired, regardless of which backend
    is used. This means saved/cached curl configs never replay a stale
    signature — the browser regenerates a fresh one every time.
  - Added _SIGNATURE_HEADERS set for any header that is per-request signed.
  - Added backend detection logging so you can confirm Playwright vs curl_cffi
    at runtime (important: Celery workers need `playwright install chromium`
    run in the same venv/environment as the web process).
"""

