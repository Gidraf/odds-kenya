# ─── Headers that must always be stripped ─────────────────────────────────────
# Browser DevTools copy-as-curl includes these. When replayed they cause
# 304 Not Modified (empty body) which we can't parse.
_STRIP = frozenset({
    "if-none-match", "if-match",
    "if-unmodified-since", "if-range",
})

# ─── Headers that must be regenerated with a fresh value each request ─────────
# if-modified-since: replaying a saved timestamp causes the server to return
# 304 Not Modified (empty body). We regenerate it to right now so the server
# always returns a full 200 response.
_REGENERATE_HEADERS: dict[str, callable] = {
    "if-modified-since": lambda: email.utils.format_datetime(
        datetime.now(timezone.utc), usegmt=True
    ),
}


# ─── Per-request signature headers ────────────────────────────────────────────
# These headers contain HMAC tokens that are generated by the site's own JS
# at request time and expire within ~1 second. They MUST be stripped from any
# saved/cached config so the browser can regenerate a fresh one each probe.
#
# Add any new per-request signature headers here as you discover them.
_SIGNATURE_HEADERS = frozenset({
    "x-hd",           # 1xbet — per-request HMAC, expires ~1s
    "x-request-sign", # generic pattern used by some bookmakers
    "x-signature",    # generic
    "x-ts",           # timestamp-based tokens
    "x-nonce",        # nonce tokens
})

import shlex
import urllib.parse

def _build_curl(
    method:  str,
    url:     str,
    headers: dict,
    params:  dict,
    body:    Any = None,
    cookies: str | None = None,
) -> str:
    """
    Build a human-readable curl command.
    - Params are properly URL-encoded (quote_via=urllib.parse.quote, not quote_plus)
    - Body is emitted as --data-raw for POST/PUT/PATCH
    - Cookies emitted as -b
    - GET requests omit the redundant -X GET flag
    - Output is multiline for readability
    """
    parts = ["curl"]

    # Only add -X for non-GET methods
    if method.upper() != "GET":
        parts.append(f"-X {method.upper()}")

    # URL + params
    qs       = urllib.parse.urlencode(params, quote_via=urllib.parse.quote) if params else ""
    full_url = f"{url}?{qs}" if qs else url
    parts.append(shlex.quote(full_url))

    # Headers
    for k, v in (headers or {}).items():
        parts.append(f"-H {shlex.quote(f'{k}: {v}')}")

    # Cookies
    if cookies:
        parts.append(f"-b {shlex.quote(cookies)}")

    # Body
    if body is not None:
        body_str = json.dumps(body) if isinstance(body, (dict, list)) else str(body)
        parts.append(f"--data-raw {shlex.quote(body_str)}")

    return " \\\n  ".join(parts)

# ─── Decompression ────────────────────────────────────────────────────────────

def _decompress(raw: bytes, encoding: str = "") -> tuple[bytes, str]:
    enc = encoding.lower()
    if "br" in enc and _BROTLI_AVAILABLE:
        try: return _brotli_mod.decompress(raw), "brotli"
        except Exception: pass
    if "gzip" in enc:
        try: return gzip.decompress(raw), "gzip"
        except Exception: pass
    if "deflate" in enc:
        for wb in (zlib.MAX_WBITS, -zlib.MAX_WBITS):
            try: return zlib.decompress(raw, wb), "deflate"
            except Exception: pass
    if len(raw) >= 2 and raw[0] == 0x78:
        for wb in (zlib.MAX_WBITS, -zlib.MAX_WBITS):
            try: return zlib.decompress(raw, wb), "zlib-magic"
            except Exception: pass
    if len(raw) >= 2 and raw[0] == 0x1F and raw[1] == 0x8B:
        try: return gzip.decompress(raw), "gzip-magic"
        except Exception: pass
    return raw, "none"


# ─── Shared helpers ───────────────────────────────────────────────────────────

def _parse_json(text: str) -> Any:
    for candidate in (text.strip(), re.sub(r"^\s*[\w$]+\s*\(", "", text).rstrip(");").strip()):
        try: return json.loads(candidate)
        except Exception: pass
    return None


def _find_array(parsed: Any) -> tuple[Any, str | None, int]:
    if isinstance(parsed, list):
        return (parsed[0] if parsed else None), None, len(parsed)
    if isinstance(parsed, dict):
        for k, v in parsed.items():
            if isinstance(v, list):
                return (v[0] if v else None), k, len(v)
    return None, None, 0


def _build_result(status: int | None, raw: bytes, encoding: str,
                  curl_cmd: str, latency: int, content_type: str = "",
                  regen_curl: str | None = None) -> dict:
    decompressed, method = _decompress(raw, encoding)
    if method != "none":
        print(f"[probe] {status} — {method} {len(raw)}→{len(decompressed)}b")
    else:
        print(f"[probe] {status} — {len(raw)}b")
    text   = decompressed.decode("utf-8", errors="replace")
    parsed = _parse_json(text)
    if parsed is None and text.strip():
        print(f"[probe] JSON parse failed. First 200: {text[:200]!r}")
    first_item, array_key, array_length = _find_array(parsed)
    ok = status is not None and 200 <= status < 300 and parsed is not None
    return {
        "ok": ok, "status": status, "latency_ms": latency,
        "content_type": content_type, "size_bytes": len(raw),
        "parsed": parsed, "response": parsed,
        "response_raw": text[:16_000],
        "first_item": first_item, "array_key": array_key, "array_length": array_length,
        "error": None if ok else f"HTTP {status}",
        "curl": curl_cmd,
        "curl_regenerated": regen_curl or curl_cmd,
    }


def _error_result(curl_cmd: str, error: str, latency: int) -> dict:
    return {
        "ok": False, "status": None, "latency_ms": latency,
        "content_type": "", "size_bytes": 0,
        "parsed": None, "response": None, "response_raw": "",
        "first_item": None, "array_key": None, "array_length": 0,
        "error": error, "curl": curl_cmd, "curl_regenerated": curl_cmd,
    }


