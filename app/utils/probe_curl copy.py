def _flatten_item(obj: Any, prefix: str = "", depth: int = 0) -> list[tuple[str, str]]:
    if depth > 5 or obj is None: return []
    if isinstance(obj, (str, int, float, bool)) and prefix: return [(prefix, str(obj))]
    if isinstance(obj, list):
        out = []
        for i, v in enumerate(obj[:5]): out.extend(_flatten_item(v, f"{prefix}[{i}]", depth+1))
        return out
    if isinstance(obj, dict):
        out = []
        for k, v in obj.items():
            path = f"{prefix}.{k}" if prefix else k
            if isinstance(v, (str, int, float, bool)): out.append((path, str(v)))
            else: out.extend(_flatten_item(v, path, depth+1))
        return out
    return []


def _field_to_var_name(field_path: str) -> str:
    leaf = field_path.split(".")[-1].split("[")[0]
    for pattern, role in _ROLE_HINTS:
        if pattern.search(leaf): return role
    clean = re.sub(r"([A-Z])", r"_\1", leaf).lower().strip("_")
    return re.sub(r"[^a-z0-9_]", "_", clean).strip("_") or "value"


def _detect_placeholders(markets_url: str, markets_params: dict,
                          list_item: dict, min_value_len: int = 4) -> dict:
    try:
        purl       = urllib.parse.urlparse(markets_url)
        url_params = dict(urllib.parse.parse_qsl(purl.query))
        base_url   = urllib.parse.urlunparse(purl._replace(query=""))
    except Exception:
        url_params = {}; base_url = markets_url

    all_params = {**markets_params, **url_params}
    flat       = _flatten_item(list_item)
    detected:        list[dict] = []
    placeholder_map: dict       = {}
    used_vars:       set        = set()

    for field_path, raw_value in flat:
        value = raw_value.strip()
        if len(value) < min_value_len: continue
        if value.lower() in ("true","false","null","none","undefined"): continue
        var_name = _field_to_var_name(field_path)
        base_var = var_name; n = 2
        while var_name in used_vars: var_name = f"{base_var}_{n}"; n += 1
        placeholder = f"{{{{{var_name}}}}}"; found = False
        if value in base_url:
            base_url = base_url.replace(value, placeholder, 1)
            detected.append({"var":var_name,"field_path":field_path,"value":value,"location":"url-path"})
            placeholder_map[var_name] = field_path; used_vars.add(var_name); found = True
        if not found:
            for pk, pv in list(all_params.items()):
                if str(pv).strip() == value:
                    all_params[pk] = placeholder
                    detected.append({"var":var_name,"field_path":field_path,"value":value,"location":f"param:{pk}"})
                    placeholder_map[var_name] = field_path; used_vars.add(var_name); found = True; break

    qs            = urllib.parse.urlencode(all_params) if all_params else ""
    suggested_url = f"{base_url}?{qs}" if qs else base_url
    return {"suggested_url":suggested_url,"suggested_params":all_params,
            "detected":detected,"placeholder_map":placeholder_map}