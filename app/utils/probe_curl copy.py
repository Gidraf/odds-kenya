def _clean(headers: dict | None) -> dict:
    """
    Process incoming headers before firing a request:

      1. STRIP      — conditional cache headers that cause 304 empty responses
                      (if-none-match, if-match, if-unmodified-since, if-range)
      2. REGENERATE — headers whose saved value is stale but the header itself
                      is still valid if refreshed (if-modified-since → now)
      3. STRIP      — per-request HMAC/signature headers (x-hd etc.) whose tokens
                      expire in ~1s and must be regenerated by the browser JS

    This means saved/cached curl configs are always safe to replay — stale
    timestamps are refreshed, stale signatures are dropped for the browser
    to regenerate, and cache-conditional headers that would give empty
    responses are removed entirely.
    """
    stripped_cache = []
    regenerated    = []
    stripped_sig   = []
    result         = {}

    for k, v in (headers or {}).items():
        kl = k.strip().lower()

        if kl in _STRIP:
            stripped_cache.append(k)
            continue

        if kl in _REGENERATE_HEADERS:
            fresh = _REGENERATE_HEADERS[kl]()
            result[k] = fresh
            regenerated.append(f"{k}: {v!r} → {fresh!r}")
            continue

        if kl in _SIGNATURE_HEADERS:
            stripped_sig.append(k)
            continue

        result[k] = v

    if stripped_cache:
        print(f"[probe] Stripped cache-conditional header(s): {', '.join(stripped_cache)}")
    if regenerated:
        print(f"[probe] Regenerated header(s): {'; '.join(regenerated)}")
    if stripped_sig:
        print(f"[probe] Stripped per-request signature header(s): {', '.join(stripped_sig)} "
              f"— browser will regenerate fresh tokens")

    return result


