@bp_monitor.route("/jobs/stream")
def jobs_stream():
    """
    GET /api/monitor/jobs/stream
    SSE stream: pushes a new event for every job written to the log.
    Connect once and watch harvest activity in real time.
    """
    @stream_with_context
    def generate():
        r = _redis()
        if not r:
            yield f"data: {json.dumps({'error': 'Redis unavailable'})}\n\n"
            return

        yield f"data: {json.dumps({'type': 'connected', 'ts': _now()})}\n\n"

        # Track the list length — new entries are prepended (lpush)
        try:
            last_len = r.llen("monitor:job_log")
        except Exception:
            last_len = 0

        last_ka = time.monotonic()
        while True:
            time.sleep(1)
            try:
                current_len = r.llen("monitor:job_log")
                new_count   = current_len - last_len
                if new_count > 0:
                    # New entries are at the head of the list
                    new_entries = r.lrange("monitor:job_log", 0, new_count - 1)
                    for raw in reversed(new_entries):   # oldest first
                        try:
                            entry = json.loads(raw)
                            yield f"data: {json.dumps({'type': 'job', **entry})}\n\n"
                        except Exception:
                            pass
                    last_len = current_len
            except Exception:
                pass

            if time.monotonic() - last_ka >= 15:
                yield f": keep-alive {_now()}\n\n"
                last_ka = time.monotonic()

    return Response(generate(), headers={
        "Content-Type":      "text/event-stream",
        "Cache-Control":     "no-cache",
        "X-Accel-Buffering": "no",
        "Connection":        "keep-alive",
    })


