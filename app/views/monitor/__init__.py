@bp_monitor.route("/dashboard")
def dashboard():
    """
    GET /api/monitor/dashboard
    Human-readable HTML dashboard that auto-refreshes every 60 seconds.
    Shows overall health, per-sport cache state, and the last 20 jobs.
    """
    report = _cache_get("monitor:report") or {}
    overall = report.get("overall", {})
    worker  = report.get("worker", {})
    totals  = report.get("totals", {})
    sports  = report.get("sports", [])
    checks  = report.get("checks", [])
    jobs    = report.get("recent_jobs", [])
    gen_at  = report.get("generated_at", "—")

    healthy    = overall.get("healthy", False)
    hdr_color  = _GREEN if healthy else _RED
    hdr_label  = "● HEALTHY" if healthy else "● ISSUES DETECTED"

    def _badge(val: bool) -> str:
        c = _GREEN if val else _RED
        t = "✔" if val else "✗"
        return f'<span style="color:{c};font-weight:bold">{t}</span>'

    def _status_color(s: str) -> str:
        return {"ok": _GREEN, "error": _RED, "empty": _YELLOW}.get(s, _GRAY)

    sport_rows = ""
    for s in sports:
        pop_color = _GREEN if s.get("populated") else _RED
        sport_rows += f"""
        <tr>
          <td>{s['sport']}</td>
          <td style="color:{pop_color}">{s['status']}</td>
          <td>{s['upcoming_count']}</td>
          <td>{s['live_count']}</td>
          <td>{s.get('upcoming_arbs',0) + s.get('live_arbs',0)}</td>
          <td>{s.get('upcoming_evs',0)  + s.get('live_evs',0)}</td>
          <td style="font-size:11px">{s.get('harvested_at_up','—')}</td>
        </tr>"""

    check_rows = ""
    for c in checks:
        check_rows += f"""
        <tr>
          <td>{_badge(c['passed'])}</td>
          <td>{c['name']}</td>
          <td style="color:{_GRAY};font-size:12px">{c.get('detail','')}</td>
        </tr>"""

