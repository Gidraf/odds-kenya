@bp_monitor.route("/report")
def get_report():
    """
    GET /api/monitor/report
    Returns the latest health report built by build_health_report task (every 60 s).
    If the report is stale or missing, builds one synchronously.
    """
    report = _cache_get("monitor:report")
    if not report:
        # Build on-demand if beat hasn't fired yet
        try:
            from app.workers.tasks_ops import build_health_report
            report = build_health_report()
        except Exception as exc:
            return {"ok": False, "error": str(exc)}, 500

    return {"ok": True, **report}


@bp_monitor.route("/report/history")
def get_report_history():
    """
    GET /api/monitor/report/history
    Returns the last 60 health report summaries (one per minute = 1 hour).
    """
    r = _redis()
    if not r:
        return {"ok": False, "error": "Redis unavailable"}, 503

    limit = min(int(request.args.get("limit", 60)), 60)
    raw   = r.lrange("monitor:report_history", 0, limit - 1)
    history = []
    for item in raw:
        try:
            history.append(json.loads(item))
        except Exception:
            pass

    return {
        "ok":      True,
        "total":   len(history),
        "history": history,
        "ts":      _now(),
    }


@bp_monitor.route("/jobs")
def get_jobs():
    """
    GET /api/monitor/jobs?limit=100&bookmaker=sp&sport=soccer&status=error
    Returns recent job log entries from the rolling Redis list.
    """
    r = _redis()
    if not r:
        return {"ok": False, "error": "Redis unavailable"}, 503

    limit     = min(int(request.args.get("limit", 100)), 500)
    bk_filter = request.args.get("bookmaker", "").lower()
    sp_filter = request.args.get("sport", "").lower()
    st_filter = request.args.get("status", "").lower()

    raw  = r.lrange("monitor:job_log", 0, 499)
    jobs = []
    for item in raw:
        try:
            entry = json.loads(item)
            if bk_filter and entry.get("bookmaker", "").lower() != bk_filter:
                continue
            if sp_filter and entry.get("sport", "").lower() != sp_filter:
                continue
            if st_filter and entry.get("status", "").lower() != st_filter:
                continue
            jobs.append(entry)
        except Exception:
            pass
        if len(jobs) >= limit:
            break

    ok_count   = sum(1 for j in jobs if j.get("status") == "ok")
    err_count  = sum(1 for j in jobs if j.get("status") == "error")
    emp_count  = sum(1 for j in jobs if j.get("status") == "empty")

    return {
        "ok":         True,
        "total":      len(jobs),
        "ok_count":   ok_count,
        "err_count":  err_count,
        "empty_count": emp_count,
        "jobs":       jobs,
        "ts":         _now(),
    }


@bp_monitor.route("/sports")
def get_sports():
    """
    GET /api/monitor/sports
    Quick per-sport cache summary without the full report payload.
    """
    from app.workers.tasks_ops import PERSIST_SPORTS

    summary = []
    for sport in PERSIST_SPORTS:
        u = _cache_get(f"combined:upcoming:{sport}") or {}
        l = _cache_get(f"combined:live:{sport}")     or {}
        u_m = u.get("matches", [])
        l_m = l.get("matches", [])
        summary.append({
            "sport":           sport,
            "upcoming":        len(u_m),
            "live":            len(l_m),
            "arbs":            sum(1 for m in u_m + l_m if m.get("has_arb")),
            "evs":             sum(1 for m in u_m + l_m if m.get("has_ev")),
            "bk_counts_up":    u.get("bk_counts", {}),
            "bk_counts_lv":    l.get("bk_counts", {}),
            "harvested_at_up": u.get("harvested_at"),
            "harvested_at_lv": l.get("harvested_at"),
            "populated":       bool(u_m or l_m),
        })

    populated = sum(1 for s in summary if s["populated"])
    return {
        "ok":              True,
        "total_sports":    len(PERSIST_SPORTS),
        "populated_sports": populated,
        "sports":          summary,
        "ts":              _now(),
    }


